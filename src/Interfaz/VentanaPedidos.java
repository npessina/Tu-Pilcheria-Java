    //Elimina un producto de la tabla de pedidos
    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        if (tablaPedidos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "No se selecciono ningún producto", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            opInterfaz.eliminarProductoPedidos(tablaPedidos, txtPrecioTotal);    
        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

    //Confirma el pedido y lo realiza. Guarda el registro de pedidos y actualiza el stock.
    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (tablaPedidos.getRowCount()==0) {
            JOptionPane.showMessageDialog(null, "La tabla esta vacia", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int eleccion = JOptionPane.showConfirmDialog(null, "¿Realizar pedido?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (eleccion == JOptionPane.YES_OPTION) {
                opBdd.guardarRegistroPedidos(log.hashMapVenta(tablaPedidos), log.obtenerFecha());
                
                if (opBdd.actualizarStockPedido(log.hashMapStock(tablaPedidos))) {
                    JOptionPane.showMessageDialog(null, "Pedido completado");
                }
                vaciar();
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    //Agrega el producto seleccionado de la tabla de productos y la cantidad a la tabla de pedidos
    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        if (tablaBuscarProductos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "No se selecciono ningún producto", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            validarCantidad();
            if (txtCantidad.getText().equals("") || txtCantidad.getText().equals("0")) {
                JOptionPane.showMessageDialog(null, "Error al ingresar la cantidad.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                opInterfaz.llenarTablaPedidos(tablaBuscarProductos, tablaPedidos, Integer.parseInt(txtCantidad.getText()), opInterfaz.verificarExistenciaCodigoTabla(tablaBuscarProductos, tablaPedidos));
                opInterfaz.obtenerPrecioTotal(tablaBuscarProductos, tablaPedidos, txtPrecioTotal, txtCantidad);
            }
        }
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

    //Boton que busca los productos en la base de datos que cumplan con los terminos
    //de busqueda y el proveedor elegido y los agrega a la tabla de productos
    private void btnBuscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProductoActionPerformed
        if (comboProveedores.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "No hay ningun proveedor cargado", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            opInterfaz.llenarTabla(opBdd.buscarProductoPedido(txtBuscarProducto.getText(),
                comboBuscarProducto.getSelectedItem().toString().toLowerCase(), comboProveedores.getSelectedItem().toString()), tablaBuscarProductos);
        }
    }//GEN-LAST:event_btnBuscarProductoActionPerformed

