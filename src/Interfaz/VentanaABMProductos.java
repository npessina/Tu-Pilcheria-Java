    //Boton que carga los campos textfield con los datos del producto seleccionado en la tabla
    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        if (tablaBuscarProductos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "No se selecciono ningún producto", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            opInterfaz.llenarComboProveedores(opBdd.obtenerProveedores(), comboModificarProveedor);
            opInterfaz.llenarCamposProducto(tablaBuscarProductos, txtID, txtModificarCodigo, txtModificarNombre,
                    comboModificarCategoriaProducto, txtModificarDescripcion, comboModificarTalle, txtModificarPrecio, comboEstado, comboModificarProveedor, txtStockMinimoModificar);
        }
        
    }//GEN-LAST:event_btnModificarProductoActionPerformed

    //Boton que guarda el producto modificado en la base de datos
    private void btnGuardarProductoModificadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoModificadoActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String talle="";
            if (comboModificarTalle.getItemCount()!=0) {
                talle=comboModificarTalle.getSelectedItem().toString();
            }
            Producto producto = opClases.crearProducto(txtModificarNombre.getText(), txtModificarCodigo.getText(), comboModificarCategoriaProducto.getSelectedItem().toString(),
                    txtModificarDescripcion.getText(), talle, txtModificarPrecio.getText(), comboModificarProveedor.getSelectedItem().toString(), txtStockMinimoModificar.getText());  
            if (validacionClase.ValidarProducto(producto)) {
                int eleccion = JOptionPane.showConfirmDialog(null, "¿Actualizar producto?", "Confirmar", JOptionPane.YES_NO_OPTION); 
                if (eleccion == JOptionPane.YES_OPTION) {
                    if (opBdd.guardarProductoModificado(producto, comboModificarCategoriaProducto.getSelectedItem().toString(), comboEstado.getSelectedItem().toString(), txtID.getText())) {
                        JOptionPane.showMessageDialog(null, "El codigo de producto ya existe. Seleccione otro.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Datos actualizados");
                        vaciarModificar();
                    }
                } 
            }
        }
    }//GEN-LAST:event_btnGuardarProductoModificadoActionPerformed

