    //Verifica si la cantidad ingresada es un numero
    public void validarCantidad(){
        Pattern patron = Pattern.compile("^\\d*$");
        Matcher m = patron.matcher(txtCantidad.getText());
        
        if (m.find()==false || txtCantidad.getText().isEmpty()) {
            txtCantidad.setText("");
        }
    }
    
