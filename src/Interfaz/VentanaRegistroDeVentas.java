    private void btnBuscarRegistroVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRegistroVentaActionPerformed
        opInterfaz.llenarTabla(opBdd.buscarRegistroVentas(txtBuscarRegistroVenta.getText(),
            comboBuscarRegistroVenta.getSelectedItem().toString().toLowerCase()), tablaBuscarRegistroVenta);
    }//GEN-LAST:event_btnBuscarRegistroVentaActionPerformed

    //Carga en la tabla los productos vendidos en la venta seleccionada
    private void btnCargarProductosVendidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarProductosVendidosActionPerformed
        if (tablaBuscarRegistroVenta.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "No se selecciono ninguna venta", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            opInterfaz.llenarTabla(opBdd.obtenerProductosVendidos(Integer.parseInt(tablaBuscarRegistroVenta.getValueAt(tablaBuscarRegistroVenta.getSelectedRow(), 0).toString())), tablaVentas);
        }
    }//GEN-LAST:event_btnCargarProductosVendidosActionPerformed

    //Genera la factura de la venta seleccionada
    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        if (tablaVentas.getRowCount()==0) {
            JOptionPane.showMessageDialog(null, "No hay elementos para imprimir.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int eleccion = JOptionPane.showConfirmDialog(null, "Â¿Generar factura?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (eleccion == JOptionPane.YES_OPTION) {
                log.imprimirRegistro(tablaVentas, tablaBuscarRegistroVenta);
            }
        }
    }//GEN-LAST:event_btnImprimirActionPerformed

    //Boton que busca los registros de ventas en la base de datos en base a la fecha
    //ingresada y los agrega a la tabla de registros de ventas
    private void btnBuscarRegistroFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRegistroFechaActionPerformed
        opInterfaz.llenarTabla(opBdd.buscarRegistroVentasPorFecha(obtenerFechaIngresada()), tablaBuscarRegistroVenta); 
    }//GEN-LAST:event_btnBuscarRegistroFechaActionPerformed

