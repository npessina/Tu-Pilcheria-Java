    //Devuelve la fecha ingresada en el JDateChooser
    public String obtenerFechaIngresada(){
        String fecha="";
        Date f = dateFecha.getDate();
        if (f!=null) {
            DateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
            fecha = formatoFecha.format(f);
        }
        return fecha;
    }
    
