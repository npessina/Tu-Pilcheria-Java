    //Boton que guarda el producto nuevo en la base de datos
    private void btnGuardarProductoNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoNuevoActionPerformed
        if (txtNuevoNombre.getText().equals("") || txtNuevoCodigo.getText().equals("")
                || txtNuevaDescripcion.getText().equals("") || txtNuevoPrecio.getText().equals("") || comboNuevoProveedor.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String talle="";
            if (comboNuevoTalle.getItemCount()!=0) {
                talle=comboNuevoTalle.getSelectedItem().toString();
            }
            Producto producto = opClases.crearProducto(txtNuevoNombre.getText(), txtNuevoCodigo.getText(), comboNuevaCategoriaProducto.getSelectedItem().toString(),
                    txtNuevaDescripcion.getText(), talle, txtNuevoPrecio.getText(), comboNuevoProveedor.getSelectedItem().toString(), txtStockMinimo.getText());  
            if (validacionClase.ValidarProducto(producto)) {
                int eleccion = JOptionPane.showConfirmDialog(null, "¿Guardar producto?", "Confirmar", JOptionPane.YES_NO_OPTION); 
                if (eleccion == JOptionPane.YES_OPTION) {
                    if (opBdd.guardarProductoNuevo(producto, comboNuevaCategoriaProducto.getSelectedItem().toString())) {
                        JOptionPane.showMessageDialog(null, "El codigo de producto ya existe. Seleccione otro.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Datos guardados");
                        vaciarNuevo();
                    }
                } 
            }
        }
    }//GEN-LAST:event_btnGuardarProductoNuevoActionPerformed

